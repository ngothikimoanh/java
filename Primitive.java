package variable;

public class Primitive {
    public static void main(String[] args) {
        // kiêu dữ liệu nguyên thủy
        int a = 5;
        boolean b = false;
        char c = 'a';
        double d = 4.5;
        System.out.println(a + " " + b + " " + c + " " + d);

        float x = a;// chuyển kiểu dữ liệu của a tư int sang float
        System.out.println(x);
    }
}
